using System;
using System.Data;
using System.Configuration;
using System.Collections;
using System.Web;
using System.Web.Security;
using System.Web.UI;
using System.Web.UI.WebControls;
using System.Web.UI.WebControls.WebParts;
using System.Web.UI.HtmlControls;
using System.IO;
using System.Collections.Generic;
using basic;
using WebApp.Components;
using System.Text;

public partial class Tools_upload : System.Web.UI.Page
{
    Products products = new Products();
    protected void Page_Load(object sender, EventArgs e)
    {
        string fenlei = basic.Tools.RequestClass.GetQueryString("fenlei");
        string Check = basic.Tools.RequestClass.GetQueryString("Check");
        string proname = basic.Tools.RequestClass.GetQueryString("proname");

        HttpPostedFile jpeg_image_upload = Request.Files["Filedata"];

        if (jpeg_image_upload.ContentLength <= WebSet.ProductFileLength * 1024)
        {
            string Isimg = System.IO.Path.GetExtension(jpeg_image_upload.FileName).ToLower();
            string filename = DateTime.Now.ToString("yyyyMMddHHmmssfff") + Isimg;
            string savePath = Server.MapPath(("~\\Upload\\") + filename);//Server.MapPath 获得虚拟服务器相对路径
            jpeg_image_upload.SaveAs(savePath);

            if (Check == "checked")
            {
                proname = Path.GetFileNameWithoutExtension(jpeg_image_upload.FileName);//只有名字
            }


            products.AddSingleProduct(fenlei, 0, proname, "", "", "", "upload/" + filename, "", "<p style=\"text-align: center;\"><img src=\"/upload/" + filename + "\"/></p>", "", System.DateTime.Now.ToString("yyyy-MM-dd"), 0);
        }
        else
        {
            Response.Write("文件大小不能超过" + WebSet.ProductFileLength + "KB！");
        }
    }
}

